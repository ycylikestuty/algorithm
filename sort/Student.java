package sort;

public class Student {
    private int score;
    private int id;
    //构造函数
    public Student(int score,int id){
        this.score=score;
        this.id=id;
    }
    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
